    public void refresh_JTable() {
        
           List technicians = CarElectronicsRepair.technicians;
           DefaultTableModel model = (DefaultTableModel) UserTable.getModel();
           Object rowData[] = new Object[6]; 
           Iterator itr = technicians.iterator();
           Technician tech;          
           model.setNumRows(0); //Remove all existing rows of JTable
           //Add every element of the List Array as a new row into JTable
           while (itr.hasNext()) {
              tech = (Technician) itr.next();
              rowData[0] = tech.getTech_id();
              rowData[1] = tech.getTech_username();
              rowData[2] = tech.getTech_contact();
              rowData[3] = tech.getTech_address();
              rowData[4] = tech.getTech_status();
              rowData[5] = tech.getTech_password();
              model.addRow(rowData);
           }   
          }
    
