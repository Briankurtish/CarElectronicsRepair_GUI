    private void AddBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddBtnActionPerformed
        // TODO add your handling code here:
        
        if(tech_id_field.getText().isEmpty() || tech_name_field.getText().isEmpty() || password_field.getText().isEmpty() || conatct_field.getText().isEmpty() || address_field.getText().isEmpty() || status_cb.getSelectedIndex() == -1)
        {
            JOptionPane.showMessageDialog(this, "Missing Information. Fill all the fields to continue");
        }
        else{
            
            int tech_id         = Integer.parseInt(this.tech_id_field.getText().trim());
            String tech_name      = this.tech_name_field.getText().trim();
            String password      = this.password_field.getText().trim();
            String contact = this.conatct_field.getText().trim();
            String address = this.address_field.getText().trim();
            String status = this.status_cb.getSelectedItem().toString().trim();
            
            
            
            
            List technicians = CarElectronicsRepair.technicians; 
            Technician tech;
            Boolean found=false;
            Iterator <Technician> itr = technicians.iterator();
            
            while (itr.hasNext()) {
              tech = itr.next(); 
              if(tech_id==tech.getTech_id()) {
                found=true;
                break;
              }
            }
            
            if(!found){
                //New Service Record
                CarElectronicsRepair.add_technicians(tech_id, tech_name, contact, address, status, password, 0);
                JOptionPane.showMessageDialog(null, "NEW Technician Record Successfully ADDED!");
            }else{
                JOptionPane.showMessageDialog(null, "Error: This Record Exists Already!");
            }
            
            refresh_JTable();
        }
    }//GEN-LAST:event_AddBtnActionPerformed

