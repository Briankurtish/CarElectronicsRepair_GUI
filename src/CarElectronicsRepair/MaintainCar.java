private void DisplayTechs_maintain() {
    
    List technicians = CarElectronicsRepair.technicians;
    DefaultTableModel model = (DefaultTableModel) Tech_table_maintain.getModel();
    Object rowData[] = new Object[3]; 
    Iterator itr = technicians.iterator();
    Technician tech;          
    model.setNumRows(0); //Remove all existing rows of JTable
    //Add every element of the List Array as a new row into JTable
    while (itr.hasNext()) {
       tech = (Technician) itr.next();
       rowData[0] = tech.getTech_id();
       rowData[1] = tech.getTech_username();
       rowData[2] = tech.getTech_status();
       model.addRow(rowData);
    }   
}

