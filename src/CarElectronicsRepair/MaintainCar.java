    private void submitBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitBtnActionPerformed
        if(cust_id.getText().isEmpty() || service_tf.getText().isEmpty() || tech_tf.getText().isEmpty() || maint_id.getText().isEmpty()) {
    JOptionPane.showMessageDialog(this, "Missing Information. Fill all the fields to continue");
} else{

            int cust_id         = Integer.parseInt(this.cust_id.getText().trim());
            int maint_id         = Integer.parseInt(this.maint_id.getText().trim());
            int service_id         = Integer.parseInt(this.service_tf.getText().trim());
            int tech_id         = Integer.parseInt(this.tech_tf.getText().trim());
            String work_status      = "Ongoing";
            


            List maintenance = CarElectronicsRepair.maintenanace;
            Maintenance maint;
            Boolean found=false;
            Iterator <Maintenance> itr = maintenance.iterator();

            while (itr.hasNext()) {
              maint = itr.next();
              if(maint_id==maint.getMaint_id()) {
                found=true;
                break;
              }
            }

            if(!found){
                //New Service Record
                CarElectronicsRepair.add_maintenance(maint_id, cust_id, service_id, tech_id, work_status);
                JOptionPane.showMessageDialog(null, "NEW Maintenance Record Successfully ADDED!");
            }else{
                JOptionPane.showMessageDialog(null, "Error: This Record Exists Already!");
            }

        }
    }//GEN-LAST:event_submitBtnActionPerformed

