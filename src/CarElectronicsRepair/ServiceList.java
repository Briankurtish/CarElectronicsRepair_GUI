    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        // TODO add your handling code here:
        
        if(service_id.getText().isEmpty() || service_name.getText().isEmpty() || price.getText().isEmpty() || description.getText().isEmpty() || status_cb.getSelectedIndex() == -1)
        {
            JOptionPane.showMessageDialog(this, "Missing Information. Fill all the fields to continue");
        }
        else{
            int service_id         = Integer.parseInt(this.service_id.getText().trim());
            String service_name      = this.service_name.getText().trim();
            int price    = Integer.parseInt(this.price.getText().trim());
            String description = this.description.getText().trim();
            String status = this.status_cb.getSelectedItem().toString().trim();
            
            
            List services = CarElectronicsRepair.services; 
            Service sv;
            Boolean found=false;
            Iterator <Service> itr = services.iterator();
            
            while (itr.hasNext()) {
              sv = itr.next(); 
              if(service_id==sv.getService_id()) {
                found=true;
                break;
              }
            }
            
            if(!found){
                //New Service Record
                CarElectronicsRepair.add_service(service_id, service_name, description, price, status);
                JOptionPane.showMessageDialog(null, "NEW Service Record Successfully ADDED!");
            }else{
                JOptionPane.showMessageDialog(null, "Error: This Record Exists Already!");
            }

            refresh_JTable();

        }
    }//GEN-LAST:event_addBtnActionPerformed

