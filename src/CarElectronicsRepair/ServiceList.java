    public void refresh_JTable() {
        
           List students = CarElectronicsRepair.services;
           DefaultTableModel model = (DefaultTableModel) serviceTable.getModel();
           Object rowData[] = new Object[5]; 
           Iterator itr = students.iterator();
           Service sv;          
           model.setNumRows(0); //Remove all existing rows of JTable
           //Add every element of the List Array as a new row into JTable
           while (itr.hasNext()) {
              sv = (Service) itr.next();
              rowData[0] = sv.getService_id();
              rowData[1] = sv.getService_name();
              rowData[2] = sv.getService_description();
              rowData[3] = sv.getService_price();
              rowData[4] = sv.getService_status();
              model.addRow(rowData);
           }   
          }
    
    
